    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            String director=(String)cbDirectors.getSelectedItem();
            repository.updateMovieDirector(selectedMovie.getiDMovie(), director);
            selectedMovie.setDirectorNameSurname(director);
            JOptionPane.showMessageDialog(null, new StringBuilder("Changes saved successfully."), "Notifications", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            Logger.getLogger(DirectorDialog.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Error", "Unable to save changes!");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

