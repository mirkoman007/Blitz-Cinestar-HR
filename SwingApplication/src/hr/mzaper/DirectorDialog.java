    private void initDirectors() throws Exception {
        DefaultComboBoxModel<String> directorsModel = new DefaultComboBoxModel<>();
        List<String> selectDirectors = repository.selectDirectors();
        selectDirectors.forEach(director->directorsModel.addElement(director));
        cbDirectors.setModel(directorsModel);
        
        
        cbDirectors.setSelectedItem(selectedMovie.getDirectorNameSurname());
    }

    private void initRepository() throws Exception {
        repository = RepositoryFactory.getRepository();
    }
