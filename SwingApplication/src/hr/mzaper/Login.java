    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if(formValid()){
            try {
                int type=repository.selectAccount(new Account(tfUsername.getText().trim(),tfPassword.getText()));
                
                if(type==-1)
                {
                    MessageUtils.showErrorMessage("Error", "Account does not exist!");
                    clearForm();
                    return;
                }                
                if(type==-2)
                {
                    MessageUtils.showErrorMessage("Error", "Wrong password!");
                    clearForm();
                    return;
                }
                
                new Home(type).setVisible(true);
                this.setVisible(false);
            } catch (Exception ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);         
                MessageUtils.showErrorMessage("Error", "Unable to login!");
            }
        }

    }//GEN-LAST:event_btnLoginActionPerformed

